const theme = createTheme();

export default function SignIn({changeLogin, profileUpdate}) {


    const [form, setform] = useState({email: "", password: "" })

    const handleChange = ((e) => {
        console.log(form)
        setform({ ...form, [e.target.name]: e.target.value })
    
      })
  
  
  
    async function handleSubmit(event) {
  
      event.preventDefault()
      const response = await fetch('http://localhost:1337/api/login', {
        method :'POST',
        headers:{
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({
          form
        })
        


      })
  
      const data = await response.json()

      if(data.jwt){

        await localStorage.setItem("JWT", data.jwt)

        await localStorage.setItem("userprofile", JSON.stringify(data.user))
        
        await changeLogin()

        await profileUpdate()

        window.location.href = "/"
      }
      else{
        alert("Please check your Email and Password")
      }
  
      console.log(data)
    }
  
  return (
    <ThemeProvider theme={theme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              onChange={handleChange}
              autoComplete="email"
              autoFocus
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              onChange={handleChange}
              id="password"
              autoComplete="current-password"
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>

            <Button
              fullWidth
              href="/createaccount"
              variant="contained"
              color="error"
            >
              Don't have an account? Sign Up
            </Button>
            <Grid container>
              <Grid item xs>
              </Grid>
              <Grid item>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}